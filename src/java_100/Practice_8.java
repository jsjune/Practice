package java_100;

public class Practice_8 {
	public static void main(String[] args) {
		
		//[1] 수치 연산자 --> +, -, *, /(몫), %(나머지)
		int a,b,c,d;
		a=60;b=8;c=300;d=400;
		
		System.out.println(a+b); //68
		System.out.println("a+b = "+a+b); //608
		System.out.println("a+b = "+(a+b)); //68
		System.out.println("a-b = "+(a-b)); //608
		System.out.println("a*b = "+(a*b)); //608
		System.out.println("a/b = "+(a/b)); //608
		System.out.println("a%b = "+(a%b)); //608
		
	}
	

}
